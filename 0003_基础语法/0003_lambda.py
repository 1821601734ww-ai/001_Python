"""
lambda 的作用：
- 快速定义一个简短的匿名函数。

什么是“匿名函数”？
- 匿名函数就是“没有名字的函数”。
- 用 lambda 定义时，不需要写 def 函数名(...)。
- 适合写一行的简单逻辑。

对比：
- 普通函数：def add(a, b): return a + b
- 匿名函数：lambda a, b: a + b

lambda 的使用限制：
- 通常只适合“一行表达式”，不适合写复杂业务逻辑。
- 不适合写多步流程（例如多个分支、循环、异常处理等）。
- 逻辑稍复杂时可读性会明显变差。

和 def 的区别（初学重点）：
- def 是完整函数定义，适合正式、可复用的函数。
- lambda 是简写，适合临时、简单的函数。
- def 里常用 return；lambda 会自动返回表达式结果。
- 经验规则：超过一行或需要长期维护，优先用 def。
"""

print("=== lambda 入门小程序 ===")

# square 和 add 是把匿名函数赋值给变量后得到的“可调用对象”
square = lambda x: x * x
add = lambda a, b: a + b

print("5 的平方：", square(5))
print("3 + 7 =", add(3, 7))
