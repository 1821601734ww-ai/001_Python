"""
in 的作用：
- 判断某个值是否“在”序列中（列表、字符串等）。

input 在本程序里的作用：
- input("提示文字") 会先显示提示，然后等待用户输入。
- 用户按回车后，input 返回“字符串”结果。
- 本例里再用 .strip() 去掉输入前后的空格，得到变量 name。
"""

print("=== in 入门小程序 ===")

students = ["小明", "小红", "小刚"]
# input 返回的是字符串，例如输入 小明，name 就是 "小明"
name = input("请输入要查找的名字：").strip()

if name in students:
    print("找到了，这位同学在名单里。")
else:
    print("没找到，这位同学不在名单里。")
