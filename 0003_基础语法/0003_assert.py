"""
assert 是什么？
- assert 叫“断言”，用来检查某个条件是否为 True。
- 如果条件为 True：程序继续往下执行。
- 如果条件为 False：程序会抛出 AssertionError，并显示你写的提示信息。

语法：
assert 条件, "条件不成立时的提示信息"
"""

print("=== assert 断言小程序 ===")

# 1) 让用户输入年龄（input 得到的是字符串）
age_text = input("请输入你的年龄（0~120）：")

# 2) 把字符串转成整数，方便做范围判断
age = int(age_text)

# 3) 使用 assert 检查条件
# 条件：年龄必须在 0 到 120 之间
assert 0 <= age <= 120, "年龄输入不合法，请输入 0~120 的数字。"

# 4) 只有断言通过，才会执行到这里
print("年龄输入合法，你输入的是：", age)